    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        if ((jTextField5.getText() != "Ingrese el id de la imagen") || (jTextField5.getText() != "")) {
            if ((jTextField6.getText() != "Ingrese el numero de capas a usar") || (jTextField6.getText() != "")) {
                try {
                    int numero = Integer.parseInt(jTextField6.getText());
                    if (numero > 0) {
                        Imagen imagen = new Imagen(jTextField5.getText());
                        ArrayList<NodoABBCapas> listaCapas = new ArrayList<>();
                        if (jComboBox1.getSelectedItem().toString().equals("Inorden")) {
                            listaCapas = claseMain.arbolABB.inOrdenLimitado(numero);
                            if (listaCapas != null) {
                                for (int i = 0; i < listaCapas.size(); i++) {
                                    imagen.insertarCapaCola(listaCapas.get(i).getId());
                                }
                                claseMain.listaImagenes.insertarImagen(imagen);
                                claseMain.listaImagenes.ordenarLista();
                                imagen.generarImagen();
                                JOptionPane.showMessageDialog(null, "Se genero la imagen");
                            }
                        } else if (jComboBox1.getSelectedItem().toString().equals("Preorden")) {
                            listaCapas = claseMain.arbolABB.preOrdenLimitado(numero);
                            if (listaCapas != null) {
                                for (int i = 0; i < listaCapas.size(); i++) {
                                    imagen.insertarCapaCola(listaCapas.get(i).getId());
                                }
                                claseMain.listaImagenes.insertarImagen(imagen);
                                claseMain.listaImagenes.ordenarLista();
                                imagen.generarImagen();
                                JOptionPane.showMessageDialog(null, "Se genero la imagen");
                            }
                        } else if (jComboBox1.getSelectedItem().toString().equals("Postorden")) {
                            listaCapas = claseMain.arbolABB.postOrdenLimitado(numero);
                            if (listaCapas != null) {
                                for (int i = 0; i < listaCapas.size(); i++) {
                                    imagen.insertarCapaCola(listaCapas.get(i).getId());
                                }
                                claseMain.listaImagenes.insertarImagen(imagen);
                                claseMain.listaImagenes.ordenarLista();
                                imagen.generarImagen();
                                JOptionPane.showMessageDialog(null, "Se genero la imagen");
                            }
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "Debe ingresar un numero mayor a 0");
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Debe ingresar un numero");
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(null, "No ingreso un id, o no es valido");
            }
        } else {
            JOptionPane.showMessageDialog(null, "No ingreso un id, o no es valido");
        }
    }//GEN-LAST:event_jButton10ActionPerformed

