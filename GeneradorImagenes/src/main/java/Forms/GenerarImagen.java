    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (jList1.getModel().getSize() > 0) {
            String selected = jList1.getSelectedValue();
            if (selected != null) {
                NodoListaDoble nodo = claseMain.listaImagenes.buscarImagen(selected);
                NodoAVLUsuario nodoUsuario = claseMain.arbolAVL.buscar(jTextField3.getText());
                Usuario usuario = nodoUsuario.getUsername();
                if (nodo != null) {
                    Imagen imagen = nodo.getImagen();
                    try {
                        claseMain.listaImagenes.eliminarImagen(selected);
                        usuario.getImagenes().eliminar(selected);
                        JOptionPane.showMessageDialog(null, "Se elimino la imagen correctamente");
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "No se logro eliminar la imagen");
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "No se encontro una imagen con ese id");
                }
            } else {
                JOptionPane.showMessageDialog(null, "No ha seleccionado la imagen");
            }

        } else {
            JOptionPane.showMessageDialog(null, "No ha buscado las imagenes de un usuario");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

