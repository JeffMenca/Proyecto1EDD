    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if ((jTextField3.getText() != "Ingrese el id del usuario") || (jTextField3.getText() != "")) {

            NodoAVLUsuario nodo = claseMain.arbolAVL.buscar(jTextField3.getText());
            DefaultListModel<String> model = new DefaultListModel<>();
            if (nodo != null) {
                Usuario usuario = nodo.getUsername();
                ColaImagenes imagenes = usuario.getImagenes();
                ArrayList<String> lista = imagenes.obtenerImagenes();
                for (int i = 0; i < lista.size(); i++) {
                    model.addElement(lista.get(i));
                }
                jList1.setModel(model);

            } else {
                JOptionPane.showMessageDialog(null, "No se encontro un usuario con ese id");
            }
        } else {
            JOptionPane.showMessageDialog(null, "No ingreso un id valido");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

